import 'package:torliga_app/domain/auth/user_entity.dart';

class UserModel extends User {
  UserModel({
    required super.id,
    required super.userName,
    required super.firstName,
    required super.lastName,
    super.phone,
    required super.email,
    super.stripeId,
    super.pmType,
    super.pmLastFour,
    super.trialEndsAt,
    required super.countryId,
    super.emailVerifiedAt,
    super.phoneVerifiedAt,
    required super.password,
    super.subscriptionEndDate,
    required super.canceledSub,
    super.socialiteName,
    super.socialiteId,
    required super.isNewUser,
    super.purchaseId,
    required super.h2hContest,
    required super.gotFreeCoin,
    super.profileUpdatedAt,
    required super.lastLoginAt,
    super.rememberToken,
    required super.createdAt,
    required super.updatedAt,
    super.deletedAt,
    required super.hasSubscription,
    required super.isVerified,
    required super.image,
  });

  factory UserModel.fromJson(Map<String, dynamic>? json) {
    return UserModel(
      id: json?['id'],
      userName: json?['username'],
      firstName: json?['first_name'],
      lastName: json?['last_name'],
      phone: json?['phone'],
      email: json?['email'],
      stripeId: json?['stripe_id'],
      pmType: json?['pm_type'],
      pmLastFour: json?['pm_last_four'],
      trialEndsAt: json?['trial_ends_at'],
      countryId: json?['country_id'],
      emailVerifiedAt: json?['email_verified_at'],
      phoneVerifiedAt: json?['phone_verified_at'],
      password: json?['password'],
      subscriptionEndDate: json?['subscription_end_date'],
      canceledSub: json?['canceled_sub'] ?? false,
      socialiteName: json?['socialite_name'],
      socialiteId: json?['socialite_id'],
      isNewUser: json?['is_new_user'] ?? false,
      purchaseId: json?['purchase_id'],
      h2hContest: json?['h2h_contest'] ?? 0,
      gotFreeCoin: json?['got_free_coin'] ?? false,
      profileUpdatedAt: json?['profile_updated_at'],
      lastLoginAt: json?['last_login_at'],
      rememberToken: json?['remember_token'],
      createdAt: json?['created_at'],
      updatedAt: json?['updated_at'],
      deletedAt: json?['deleted_at'],
      hasSubscription: json?['hasSubscription'] ?? false,
      isVerified: json?['isVerified'] ?? false,
      image: json?['image'] ?? '',
    );
  }
}